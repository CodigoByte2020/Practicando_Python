def sumar(operador1, operador2):
    print("El resultado de la suma es: " + str(operador1 + operador2))

def restar(operador1, operador2):
    print("El resultado de la resta es: " + str(operador1 - operador2))

def multiplicar(operador1, operador2):
    print("El resultado de la multiplicación es: " + str(operador1 * operador2))

def dividir(operador1, operador2):
    print("El resultado de la división es: " + str(operador1 / operador2))

def potencia(base, exponente):
    print("El resultado de la potencia es: " + str(base ** exponente))

def redondear(numero):
    print("El número redondeado es: " + str(round(numero)))
